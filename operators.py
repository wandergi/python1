# Arithmetic operators - helps in performing mathematics operations
s = 67
t = 3
print(s+t)
print(s-t)
print(s*t)
print(s/t)
# % is actually referred to as a modulus - divides the two values and returns the remainder
print(s % t)
# comparison operator
x = 34
y = 56
print(x < y)  # less than
print(x > y)  # Greater than
print(x <= y)  # less or equal to
print(x >= y)  # Greater or equal tp
print(x == y)  # equal to
print(x != y)  # not equal to
# Assignment operators - help in assigning the value on variable
a = 13
b = 4
b += 67
a -= 45
print(b)
print(a)
# Logical Operators -
f = 20
u = 23
print(f > u > f)  # and returns a true if all the conditions are true  hence automatically prints a false
print(f > u or f < u)  #
print(not (f > u or f < u))  # it reverses the final result
